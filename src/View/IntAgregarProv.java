    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if(datosValidos){
            if(txtRfc.getText().length()!=0 && txtRznSoc.getText().length()!=0 && txtDireccion.getText().length()!=0 && txtTel.getText().length()!=0 && txtMail.getText().length()!=0){
                Proveedor p = new Proveedor(txtRfc.getText(),txtRznSoc.getText(),txtDireccion.getText(),txtTel.getText(),txtMail.getText());
                if(p.agregarProv(p)){
                    JOptionPane.showOptionDialog(this, "Registro Insertado", "Notificación", JOptionPane.INFORMATION_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{" OK "},"OK");         
                    dispose();
                    IntProveedores v = new IntProveedores();
                    v.setVisible(true);
                }else{
                    JOptionPane.showOptionDialog(this, "Verifique sus datos", "ERROR", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" OK "},"OK"); 
                }
            }else{
                JOptionPane.showOptionDialog(this, "Campos vacíos", "Aviso", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" OK "},"OK"); 
            }
        }else{
            JOptionPane.showOptionDialog(this, "Datos no válidos", "Aviso", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" OK "},"OK"); 
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

