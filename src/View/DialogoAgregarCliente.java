    private void txtSaldoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSaldoKeyReleased
        if(Pattern.matches("^([0-9]{1,6})(([.]{1})([0-9]{1,2}))*$",txtSaldo.getText())){
            txtSaldo.setBackground(Color.GREEN);
            txtSaldo.setForeground(Color.BLACK);
            this.datosValidos = true;
        }
        else{
            txtSaldo.setForeground(Color.WHITE);
            txtSaldo.setBackground(Color.RED);
            this.datosValidos = false;
        }
    }//GEN-LAST:event_txtSaldoKeyReleased

