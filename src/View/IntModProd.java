    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Boolean nombre = false;
        Boolean marca = false;
        Boolean tipo = false;
        Boolean precioV = false;
        String valores= "";
        if(datosValidos){
            if(txtNombre.getText().length()!=0 && txtMarca.getText().length()!=0 && txtPrecioV.getText().length()!=0){
                if(!prod.getNombre().equals(txtNombre.getText())){
                    nombre = true;
                    valores += "nombre_prod = '" + txtNombre.getText() + "' ";
                }
                else if(!prod.getMarca().equals(txtMarca.getText())){
                    marca = true;
                    if(nombre)
                        valores += ", ";
                    else
                        valores += "marca_prod = '" + txtMarca.getText() + "' ";
                }
                else if(!prod.getTipo().equals(cmbTipo.getSelectedItem().toString())){
                    tipo = true;
                    if(nombre || marca)
                        valores += ", ";
                    else
                        valores += "tipo_prod = '" + cmbTipo.getSelectedItem().toString() + "' ";
                }
                else if(prod.getPrecioV() != Double.parseDouble(txtPrecioV.getText())){
                    precioV = true;
                    if(nombre || marca || tipo)
                        valores += ", ";
                    else
                        valores += "precioV_prod = '" + txtPrecioV.getText() + "' ";
                }
                if(nombre || marca || tipo || precioV){
                    
                    Productos p = new Productos();
                    if(p.actualizarProducto(valores,prod.getCodigoBarras())){
                        uts.ventanaInfo(this, "Datos actualizados", "Aviso");
                        vP.setEnabled(true);
                        vP.actualizarDatos();
                        dispose();
                    }else{
                        uts.ventanaError(this, "Error al actualizar datos", "Aviso");
                        vP.setEnabled(true);
                        dispose();
                    }
                }else{
                    uts.ventanaError(this,"No hay modificaciones", "Aviso");
                    vP.setEnabled(true);
                    vP.actualizarDatos();
                    dispose();
                }
            }else{
                JOptionPane.showOptionDialog(this, "Datos no válidos", "Aviso", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" OK "},"OK");
            }
        }else{
            JOptionPane.showOptionDialog(this, "Campos vacíos", "Aviso", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" OK "},"OK");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

